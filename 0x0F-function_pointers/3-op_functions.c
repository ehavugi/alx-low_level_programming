/**
 * op_add - add a to b
 * @a: input a
 * @b: integer b
 *
 * Return: a +b;
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub b from a
 * @a: input 1
 * @b: input 2
 *
 * Return: a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a and b
 * @a: input 1
 * @b: input 2
 *
 * Return: a x b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a by b
 * @a: input 1
 * @b: input 2
 *
 * Return: a/b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of division of a by b
 * @a: input 1
 * @b: input 2
 *
 * Return: a%b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
